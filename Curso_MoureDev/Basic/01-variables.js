//var es la primera manera de js en crear variable por lo que en la actualidad ya no es recomendable crear variables con var

var helloWorld = "Hello JS, estoy en una variable"
console.log(helloWorld)

helloWorld = "Hello JS, acabo de cambiar el valor de la variable"
console.log(helloWorld)

// let esta manera es recomendada si solo se usa dentro de un bloque de codigo
let helloWorld2 = "Hola mundo estoy en una variable con let"
console.log(helloWorld2)

// const podemos acceder a ella pero sin modificarla, el valor que se asigne sera el definitivo
const helloWorld3 = "Hola mundo estoy en una variable con const"
console.log(helloWorld3)

